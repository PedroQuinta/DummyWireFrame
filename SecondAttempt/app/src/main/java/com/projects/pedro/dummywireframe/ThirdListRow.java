package com.projects.pedro.dummywireframe;

public class ThirdListRow {

    private String descriptionA, descriptionB;

    public ThirdListRow(String descriptionA, String descriptionB) {
        this.descriptionA = descriptionA;
        this.descriptionB = descriptionB;
    }


    public String getDescriptionA() {
        return descriptionA;
    }

    public String getDescriptionB() {
        return descriptionB;
    }
}
